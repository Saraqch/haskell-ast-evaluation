{-# LANGUAGE ImportQualifiedPost #-}

module Main where

-- ghc-lib-parser 9.6.7.20250325
-- AST extraction test using the real GHC parser

import Data.Data (Data)
import GHC.Data.EnumSet qualified as EnumSet
import GHC.Data.FastString (mkFastString)
import GHC.Data.StringBuffer (stringToStringBuffer)
import GHC.Hs
import GHC.Hs.Dump (BlankEpAnnotations (..), BlankSrcSpan (..), showAstData, showAstDataFull)
import GHC.Hs.Extension (GhcPs)
import GHC.LanguageExtensions (Extension)
import GHC.Parser (parseModule)
import GHC.Parser.Lexer
  ( ParseResult (..),
    ParserOpts,
    getPsErrorMessages,
    initParserState,
    mkParserOpts,
    unP,
  )
import GHC.Types.SrcLoc (GenLocated (..), Located, mkRealSrcLoc, unLoc)
import GHC.Utils.Error (DiagOpts (..))
import GHC.Utils.Outputable (Outputable, defaultSDocContext, ppr, showSDocUnsafe)
import System.Directory (canonicalizePath)
import System.Environment (getArgs)

main :: IO ()
main = do
  args <- getArgs
  case args of
    [path] -> processFile path
    _ -> putStrLn "Usage: cabal run ghc-lib-parser-test -- path/to/file.hs"

processFile :: FilePath -> IO ()
processFile path = do
  absolutePath <- canonicalizePath path
  putStrLn $ "Input file: " ++ absolutePath
  content <- readFile absolutePath

  let opts =
        mkParserOpts
          EnumSet.empty -- no extra extensions
          emptyDiagOpts -- minimal diagnostic options
          [] -- pragmas
          False -- safe imports
          False -- keep Haddock tokens
          False -- keep comments
          False -- magic hash
      buffer = stringToStringBuffer content
      loc = mkRealSrcLoc (mkFastString absolutePath) 1 1
      parserState = initParserState opts buffer loc

  case unP parseModule parserState of
    PFailed pst -> do
      putStrLn "Parse error:"
      putStrLn (render (getPsErrorMessages pst))
    POk _ ast -> do
      putStrLn "\n 1. Raw AST generated by the library"
      putStrLn (renderRawAst ast)
      putStrLn "\n 2. Simplified AST for reading"
      putStrLn (renderReadableAst ast)
      putStrLn "\n 3. Manual AST traversal"
      exploreModule (unLoc ast)

emptyDiagOpts :: DiagOpts
emptyDiagOpts =
  DiagOpts
    { diag_warning_flags = EnumSet.empty,
      diag_fatal_warning_flags = EnumSet.empty,
      diag_warn_is_error = False,
      diag_reverse_errors = False,
      diag_max_errors = Nothing,
      diag_ppr_ctx = defaultSDocContext
    }

render :: (Outputable a) => a -> String
render = showSDocUnsafe . ppr

renderRawAst :: (Data a) => a -> String
renderRawAst = showSDocUnsafe . showAstDataFull

renderReadableAst :: (Data a) => a -> String
renderReadableAst = showSDocUnsafe . showAstData BlankSrcSpan BlankEpAnnotations

exploreModule :: HsModule GhcPs -> IO ()
exploreModule hsModule = do
  let declarations = hsmodDecls hsModule
  putStrLn $ "Declarations found: " ++ show (length declarations)
  mapM_ (exploreDeclaration . unLoc) declarations

exploreDeclaration :: HsDecl GhcPs -> IO ()
exploreDeclaration (ValD _ (FunBind _ name mg)) = do
  putStrLn "* Type      : function definition"
  putStrLn $ "* Name      : " ++ render name
  mapM_ (exploreMatch . unLoc) (unLoc (mg_alts mg))
exploreDeclaration other =
  putStrLn $ "* Declaration not covered: " ++ render other

exploreMatch :: Match GhcPs (LHsExpr GhcPs) -> IO ()
exploreMatch match = do
  putStrLn $ "* Parameters: " ++ show (length (m_pats match))
  exploreGRHSs (m_grhss match)

exploreGRHSs :: GRHSs GhcPs (LHsExpr GhcPs) -> IO ()
exploreGRHSs (GRHSs _ grhss _) = mapM_ (exploreGRHS . unLoc) grhss

exploreGRHS :: GRHS GhcPs (LHsExpr GhcPs) -> IO ()
exploreGRHS (GRHS _ _ body) = do
  putStrLn $ "* Body      : " ++ render body
  exploreExpression (unLoc body)

exploreExpression :: HsExpr GhcPs -> IO ()
exploreExpression (OpApp _ lhs op rhs) = do
  putStrLn "* Expression: infix application (binary operator)"
  putStrLn $ "  - Left      : " ++ render lhs
  putStrLn $ "  - Operator  : " ++ render op
  putStrLn $ "  - Right     : " ++ render rhs
exploreExpression other =
  putStrLn $ "* Expression not covered: " ++ render other